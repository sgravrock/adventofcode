import XCTest

class day4_2_tests: XCTestCase {
	func test_isValid() {
		let maxValid: [UInt8] = [0x0, 0x0, 0x0, 0xff, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0]
		let minInvalid: [UInt8] = [0x0, 0x0, 0x01, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0]
		let otherDigit: [UInt8] = [0x0, 0x01, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0]
		XCTAssertTrue(isValid(hash: maxValid))
		XCTAssertFalse(isValid(hash: minInvalid))
		XCTAssertFalse(isValid(hash: otherDigit))
	}
}
